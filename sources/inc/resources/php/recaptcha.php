// Checke ob Captcha richtig eingegeben wurde
// Gibt TRUE zurck, wenn der Captcha Code richtig eingegeben wurde
function getCaptchaStatus() {

	global $GoogleReCaptchaPrivateKey;
	
	// Validiere Captcha
	$qry = recaptcha_check_answer($GoogleReCaptchaPrivateKey, $_SERVER["REMOTE_ADDR"], $_POST["recaptcha_challenge_field"], $_POST["recaptcha_response_field"]);
	
	//Checke ob Captcha stimmt
	if ($qry->is_valid) {
		$return = TRUE;
		} else {
			$return = FALSE;
	}
	
	return $return;
	                                
}

